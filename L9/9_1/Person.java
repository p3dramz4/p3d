package tamrin_1;

public class Person {
    int id ;
    String name;
    int age;
    String email;
    String job;

    public Person(int id, String name, int age, String email, String job) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.email = email;
        this.job = job;
    }
}